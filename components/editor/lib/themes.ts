export function applyTheme(rootEl: HTMLElement, id: string): Theme {
  const theme = THEMES.find((t) => t.id === id) ?? THEMES[0];
  for (const [k, v] of Object.entries(theme.vars)) rootEl.style.setProperty(k, v);
  return theme;
}
